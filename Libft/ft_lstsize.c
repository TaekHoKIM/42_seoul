#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	count;

	count = 0;
	if (lst == NULL)
		return (count);
	else
		count++;
	while (lst->next != NULL)
	{
		lst = lst->next;
		count++;
	}
	return (count);
}
